    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        // TODO add your handling code here:
 
        DefaultTableModel model = (DefaultTableModel)tbldrugs_sold.getModel();
        setValues();
        trans.add();
        trandrug.setTranID(Integer.parseInt(txttranid.getText()));
                
        for (int x=0 ; x < tbldrugs_sold.getRowCount() ; x++){
            int Dcode = Integer.parseInt(drug.getValueByName(tbldrugs_sold.getValueAt(x, 0).toString()));
            int amount_sold = Integer.parseInt(tbldrugs_sold.getValueAt(x, 1).toString());
            trandrug.setDrugName(tbldrugs_sold.getValueAt(x, 0).toString());
            trandrug.setDrugCode(Dcode);
            trandrug.setAmount(Integer.parseInt(tbldrugs_sold.getValueAt(x, 1).toString()));
            trandrug.add();
            
            String select = "select amount from drug" + " where drugcode = " + Dcode;
            int old_amount = Integer.parseInt((String)DB.db.getTableData(select).Items[0][0]);
            int new_amount = old_amount - amount_sold;
            String strUpdate = "update drug set " + "amount = " + new_amount +" where drugcode = " + Dcode;
            DB.db.runNonQuery(strUpdate);
        }
        model.setNumRows(0);
        clearData();
        trans.getAllRows(tbltrans);
                                    

    }//GEN-LAST:event_btnaddActionPerformed

