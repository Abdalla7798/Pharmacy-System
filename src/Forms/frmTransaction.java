    private void btnclaculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnclaculateActionPerformed
        // TODO add your handling code here:
        double totalprice = 0;
        for (int x=0 ; x < tbldrugs_sold.getRowCount() ; x++){
            String drugName = tbldrugs_sold.getValueAt(x, 0).toString();
            int Dcode = Integer.parseInt(drug.getValueByName(drugName));
            int amount_sold = Integer.parseInt(tbldrugs_sold.getValueAt(x, 1).toString());
            
            String select = "select price from drug" + " where drugcode = " + Dcode;
            double price = Double.parseDouble((String)DB.db.getTableData(select).Items[0][0]);
            totalprice = totalprice + amount_sold * price;
        }
        txttotalprice.setText(String.valueOf(totalprice));
    }//GEN-LAST:event_btnclaculateActionPerformed

