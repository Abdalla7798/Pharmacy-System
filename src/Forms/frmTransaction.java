    private void tbltransMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbltransMouseClicked
        try {
            // TODO add your handling code here:
            int row = tbltrans.getSelectedRow();
            String strtranid = tbltrans.getValueAt(row, 0).toString();
            String struserid = tbltrans.getValueAt(row, 1).toString();
            String customerName = tbltrans.getValueAt(row, 2).toString();
            String customerPhone = tbltrans.getValueAt(row, 3).toString();
            String Totalprice = tbltrans.getValueAt(row, 4).toString();
            

            txttranid.setText(strtranid);
            txtuserid.setText(struserid);
            txtcustomerName.setText(customerName);
            txtcustomerPhone.setText(customerPhone);
            txttotalprice.setText(Totalprice);
            
            

            trandrug.setTranID(Integer.parseInt(txttranid.getText()));
            trandrug.getAllRows(tbldrugs_sold);

            btnadd.setEnabled(false);

        } catch (Exception ex) {
            Tool.msgBox(ex.getMessage());
        }

    }//GEN-LAST:event_tbltransMouseClicked

