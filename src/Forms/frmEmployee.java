    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        try {
            if (Tool.confirmMsg("Do you want to delete?")){
                User employee = null; 
                
                if (rdopharmacymanager.isSelected()){
                    employee = new PharmacyManager();
                }
                else if (rdopharmacist.isSelected()){
                    employee = new Pharmacist();
                }
                else {
                    employee = new Cashier();
                }
                
                int empID = Integer.parseInt(txtempid.getText());
                if (empID == 1){
                   Tool.msgBox("Can't delete this Employee");
                }
                else{
                   employee.setUserID(empID);
                   manager.delete(employee);
                   clearData();
                   //fillTable("select * from user",tblemp); 
                }    
            }

        } catch (Exception ex) {
            Tool.msgBox(ex.getMessage());
        }

    }//GEN-LAST:event_btndeleteActionPerformed

