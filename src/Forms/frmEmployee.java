    private void tblempMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblempMouseClicked
        try {
            // TODO add your handling code here:
            int row = tblemp.getSelectedRow();
            String strUserID = tblemp.getValueAt(row, 0).toString();
            String strUsername = tblemp.getValueAt(row, 1).toString();
            String strName = tblemp.getValueAt(row, 2).toString();
            String address = tblemp.getValueAt(row, 3).toString();
            String phone = tblemp.getValueAt(row, 4).toString();
            String type = tblemp.getValueAt(row, 5).toString();
  
            txtempid.setText(strUserID);
            txtusername.setText(strUsername);
            txtname.setText(strName);
            txtaddress.setText(address);
            txtphone.setText(phone);
            txtpassword.setText("");
            
            if(type.equals("Pharmacy Manager")){
               rdopharmacymanager.setSelected(true);
            }
            else if (type.equals("Pharmacist")){
               rdopharmacist.setSelected(true);
            }
            else{
               rdocashier.setSelected(true);
            }
            
            rdopharmacymanager.setEnabled(false);
            rdopharmacist.setEnabled(false);
            rdocashier.setEnabled(false);
            txtpassword.setEnabled(false);
            
            btnadd.setEnabled(false);
            btndelete.setEnabled(true);

        } catch (Exception ex) {
            Tool.msgBox(ex.getMessage());
        }
    }//GEN-LAST:event_tblempMouseClicked

