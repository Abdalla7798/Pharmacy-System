    private void setValues(){
        //drug.setDrugCode(Integer.parseInt(txtdrugcode.getText()));
        emp.setUserID(Integer.parseInt(txtempid.getText()));
        emp.setUserName(txtusername.getText());
        emp.setPassword(txtpassword.getText());
        emp.setName(txtname.getText());
        emp.setAddress(txtaddress.getText());
        emp.setPhone(txtphone.getText());
        
        if (rdopharmacymanager.isSelected()){
           emp.setType(rdopharmacymanager.getText());
        }
        else if (rdopharmacist.isSelected()){
           emp.setType(rdopharmacist.getText()); 
        }
        else{
           emp.setType(rdocashier.getText()); 
        }     
    }
    
    private void clearData(){
        Tool.clearText(this);
        txtempid.setText(manager.getAutoNumber());
        btnadd.setEnabled(true);
        btndelete.setEnabled(false);
        rdopharmacymanager.setEnabled(true);
        rdopharmacist.setEnabled(true);
        rdocashier.setEnabled(true);
        txtpassword.setEnabled(true);
        fillTable("select * from user",tblemp);
        txtusername.requestFocus();
    }
    
    private void fillTable (String statement ,JTable table){
        
        DefaultTableModel model =(DefaultTableModel)table.getModel();
        model.setNumRows(0);
        Tool.Table t = DB.db.getTableData(statement);
        
        for (int i = 0 ; i < t.Items.length ; i++){
           model.addRow(new Object[]{t.Items[i][0], t.Items[i][1], t.Items[i][3], 
               t.Items[i][4], t.Items[i][5], t.Items[i][6]}); 
        }
    }
    
